class Solution {
public:
    vector<int> allPrimes={
        2, 5, 17, 41, 197, 281, 7699, 8893, 22039, 24133, 25237, 28697, 32353, 37561, 38921, 43201, 44683, 55661, 62981, 75571,
        78623, 84859, 102547, 105943, 112909, 116447, 127241, 134489, 138173, 153301, 169367, 173507, 177691, 203807, 212869, 226757, 231497, 241117, 255883, 276137, 286549, 297121, 318781, 329867, 341071, 358327, 364141, 375893, 387841, 399917, 418357, 424567
    };
    int largestPrime(int n){
        if(n==1) return 0;
        if(n<5) return 2;
        auto lb=lower_bound(allPrimes.begin(), allPrimes.end(), n);
        return *prev(lb);
    }
};
